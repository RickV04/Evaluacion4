    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
    Futbolista LPT2 = new Futbolista();
    LPT2.setNombre(nombre.getText());
    LPT2.setApellidos(apellidos.getText());
    LPT2.setEdad(Integer.parseInt(edad.getText()));
    LPT2.setId(Integer.parseInt(id.getText()));
    LPT2.setDemarcacion(demarcacion.getText());
    
    lista.add(LPT2);
    
    this.mostrar();
    
    
    }//GEN-LAST:event_BtnAgregarActionPerformed
    
    private void mostrar(){
    String titulos[] = {"ID", "Nombre", "Apellidos", "Edad","Dermarcacion", "Dorsal"};
        tbl.setColumnIdentifiers(titulos);
        this.tbl1.setModel(tbl);
        for(Futbolista l: lista){
            Object[] datos = new Object[6];
            datos[0]=l.getId();
            datos[1]=l.getNombre();
            datos[2]=l.getApellidos();
            datos[3]=l.getEdad();
            datos[4]=l.getDemarcacion();
            datos[5]=l.getDorsal();
            tbl.addRow(datos);
        }
}
    
